#include "pch.h"
#include "handles.h"

//---------------------------------------------------------------------------------------------------------
//---------------------------------------------------------------------------------------------------------
curl_easy::curl_easy()
	: eh_{curl_easy_init()}
{}
//---------------------------------------------------------------------------------------------------------
curl_easy::curl_easy(curl_easy&& src)
	: eh_{src.eh_}
{
	src.eh_ = nullptr;
}
//---------------------------------------------------------------------------------------------------------
curl_easy::~curl_easy()
{
	if (eh_)
	{
		curl_easy_cleanup(eh_);
		eh_ = nullptr;
	}
}
//---------------------------------------------------------------------------------------------------------
curl_multi::curl_multi()
	: multi_handle_{curl_multi_init()}
{
}
//---------------------------------------------------------------------------------------------------------
curl_multi::~curl_multi()
{
	if (multi_handle_)
	{
		curl_multi_cleanup(multi_handle_);
		multi_handle_ = nullptr;
	}
}

//---------------------------------------------------------------------------------------------------------
//---------------------------------------------------------------------------------------------------------
curl_global::curl_global()
{
	curl_global_init(0);
}
//---------------------------------------------------------------------------------------------------------
curl_global::~curl_global()
{
	curl_global_cleanup();
}
